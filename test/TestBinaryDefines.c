#include "unity.h"
#include "BinaryDefines.h"

void test_B_Macro_ConvertsFromBinary(void)
{
  TEST_ASSERT_EQUAL_UINT16(0, B(0));
  TEST_ASSERT_EQUAL_UINT16(1, B(1));
  //width of 1

  TEST_ASSERT_EQUAL_UINT16(0, B(00));
  TEST_ASSERT_EQUAL_UINT16(1, B(01));
  TEST_ASSERT_EQUAL_UINT16(2, B(10));
  TEST_ASSERT_EQUAL_UINT16(3, B(11));
  //width of 2

  TEST_ASSERT_EQUAL_UINT16(0, B(000));
  TEST_ASSERT_EQUAL_UINT16(1, B(001));
  TEST_ASSERT_EQUAL_UINT16(2, B(010));
  TEST_ASSERT_EQUAL_UINT16(3, B(011));
  TEST_ASSERT_EQUAL_UINT16(4, B(100));
  TEST_ASSERT_EQUAL_UINT16(5, B(101));
  TEST_ASSERT_EQUAL_UINT16(6, B(110));
  TEST_ASSERT_EQUAL_UINT16(7, B(111));
  //width of 3

  TEST_ASSERT_EQUAL_UINT16(0, B(0000));
  TEST_ASSERT_EQUAL_UINT16(1, B(0001));
  TEST_ASSERT_EQUAL_UINT16(2, B(0010));
  TEST_ASSERT_EQUAL_UINT16(3, B(0011));
  TEST_ASSERT_EQUAL_UINT16(4, B(0100));
  TEST_ASSERT_EQUAL_UINT16(5, B(0101));
  TEST_ASSERT_EQUAL_UINT16(6, B(0110));
  TEST_ASSERT_EQUAL_UINT16(7, B(0111));
  TEST_ASSERT_EQUAL_UINT16(8, B(1000));
  TEST_ASSERT_EQUAL_UINT16(9, B(1001));
  TEST_ASSERT_EQUAL_UINT16(10, B(1010));
  TEST_ASSERT_EQUAL_UINT16(11, B(1011));
  TEST_ASSERT_EQUAL_UINT16(12, B(1100));
  TEST_ASSERT_EQUAL_UINT16(13, B(1101));
  TEST_ASSERT_EQUAL_UINT16(14, B(1110));
  TEST_ASSERT_EQUAL_UINT16(15, B(1111));
  //width of 4

  TEST_ASSERT_EQUAL_UINT16(0, B(00000));
  TEST_ASSERT_EQUAL_UINT16(1, B(00001));
  TEST_ASSERT_EQUAL_UINT16(2, B(00010));
  TEST_ASSERT_EQUAL_UINT16(3, B(00011));
  TEST_ASSERT_EQUAL_UINT16(4, B(00100));
  TEST_ASSERT_EQUAL_UINT16(5, B(00101));
  TEST_ASSERT_EQUAL_UINT16(6, B(00110));
  TEST_ASSERT_EQUAL_UINT16(7, B(00111));
  TEST_ASSERT_EQUAL_UINT16(8, B(01000));
  TEST_ASSERT_EQUAL_UINT16(9, B(01001));
  TEST_ASSERT_EQUAL_UINT16(10, B(01010));
  TEST_ASSERT_EQUAL_UINT16(11, B(01011));
  TEST_ASSERT_EQUAL_UINT16(12, B(01100));
  TEST_ASSERT_EQUAL_UINT16(13, B(01101));
  TEST_ASSERT_EQUAL_UINT16(14, B(01110));
  TEST_ASSERT_EQUAL_UINT16(15, B(01111));
  TEST_ASSERT_EQUAL_UINT16(16, B(10000));
  TEST_ASSERT_EQUAL_UINT16(17, B(10001));
  TEST_ASSERT_EQUAL_UINT16(18, B(10010));
  TEST_ASSERT_EQUAL_UINT16(19, B(10011));
  TEST_ASSERT_EQUAL_UINT16(20, B(10100));
  TEST_ASSERT_EQUAL_UINT16(21, B(10101));
  TEST_ASSERT_EQUAL_UINT16(22, B(10110));
  TEST_ASSERT_EQUAL_UINT16(23, B(10111));
  TEST_ASSERT_EQUAL_UINT16(24, B(11000));
  TEST_ASSERT_EQUAL_UINT16(25, B(11001));
  TEST_ASSERT_EQUAL_UINT16(26, B(11010));
  TEST_ASSERT_EQUAL_UINT16(27, B(11011));
  TEST_ASSERT_EQUAL_UINT16(28, B(11100));
  TEST_ASSERT_EQUAL_UINT16(29, B(11101));
  TEST_ASSERT_EQUAL_UINT16(30, B(11110));
  TEST_ASSERT_EQUAL_UINT16(31, B(11111));
  //width of 5
}

int main(void)
{
  UNITY_BEGIN();
  RUN_TEST(test_B_Macro_ConvertsFromBinary);
  return UNITY_END();
}
